
import { useState } from 'react';
import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/card';
import { Button } from '@/components/ui/button';
import { Badge } from '@/components/ui/badge';
import { CheckCircle, Clock, User, MapPin, Calendar, AlertTriangle } from 'lucide-react';
import { useHotelData } from '@/hooks/useHotelData';
import { BackToHomeButton } from '@/components/ui/back-to-home-button';
import { format } from 'date-fns';
import { es } from 'date-fns/locale';
import { useToast } from '@/hooks/use-toast';
import { Reservation } from '@/types/hotel';

const CheckInOutPage = () => {
  const { reservations, guests, rooms, updateReservation, isLoading } = useHotelData();
  const { toast } = useToast();
  const [processingReservations, setProcessingReservations] = useState<Set<string>>(new Set());

  const today = new Date().toISOString().split('T')[0];

  // Filtrar reservas por categor√≠as
  const todayCheckIns = reservations.filter(r => 
    r.check_in === today && r.status === 'confirmed'
  );
  const todayCheckOuts = reservations.filter(r => 
    r.check_out === today && r.status === 'checked-in'
  );
  const currentGuests = reservations.filter(r => r.status === 'checked-in');
  const earlyCheckIns = reservations.filter(r => 
    r.check_in > today && r.status === 'confirmed'
  );
  const lateCheckOuts = reservations.filter(r => 
    r.check_out < today && r.status === 'checked-in'
  );

  const handleCheckIn = async (reservationId: string) => {
    if (processingReservations.has(reservationId)) return;
    
    console.log('üîÑ INICIANDO CHECK-IN:', reservationId);
    
    const newProcessing = new Set(processingReservations);
    newProcessing.add(reservationId);
    setProcessingReservations(newProcessing);

    try {
      console.log('‚è≥ ACTUALIZANDO ESTADO A CHECKED-IN...');
      
      await updateReservation({ 
        id: reservationId, 
        status: 'checked-in' as Reservation['status']
      });
      
      const reservation = reservations.find(r => r.id === reservationId);
      const guest = reservation ? guests.find(g => g.id === reservation.guest_id) : null;
      const room = reservation ? rooms.find(r => r.id === reservation.room_id) : null;
      
      console.log('‚úÖ CHECK-IN COMPLETADO');
      
      toast({
        title: "‚úÖ Check-in realizado",
        description: guest && room 
          ? `${guest.first_name} ${guest.last_name} en habitaci√≥n ${room.number}`
          : "Check-in completado exitosamente",
      });
    } catch (error) {
      console.error('‚ùå ERROR EN CHECK-IN:', error);
      toast({
        title: "‚ùå Error en check-in",
        description: "No se pudo realizar el check-in",
        variant: "destructive",
      });
    } finally {
      const newProcessing = new Set(processingReservations);
      newProcessing.delete(reservationId);
      setProcessingReservations(newProcessing);
    }
  };

  const handleCheckOut = async (reservationId: string) => {
    if (processingReservations.has(reservationId)) return;
    
    console.log('üîÑ INICIANDO CHECK-OUT:', reservationId);
    
    const newProcessing = new Set(processingReservations);
    newProcessing.add(reservationId);
    setProcessingReservations(newProcessing);

    try {
      console.log('‚è≥ ACTUALIZANDO ESTADO A CHECKED-OUT...');
      
      await updateReservation({ 
        id: reservationId, 
        status: 'checked-out' as Reservation['status']
      });
      
      const reservation = reservations.find(r => r.id === reservationId);
      const guest = reservation ? guests.find(g => g.id === reservation.guest_id) : null;
      const room = reservation ? rooms.find(r => r.id === reservation.room_id) : null;
      
      console.log('‚úÖ CHECK-OUT COMPLETADO');
      
      toast({
        title: "‚úÖ Check-out realizado",
        description: guest && room 
          ? `${guest.first_name} ${guest.last_name} - Habitaci√≥n ${room.number} liberada`
          : "Check-out completado exitosamente",
      });
    } catch (error) {
      console.error('‚ùå ERROR EN CHECK-OUT:', error);
      toast({
        title: "‚ùå Error en check-out",
        description: "No se pudo realizar el check-out",
        variant: "destructive",
      });
    } finally {
      const newProcessing = new Set(processingReservations);
      newProcessing.delete(reservationId);
      setProcessingReservations(newProcessing);
    }
  };

  const ReservationCard = ({ 
    reservation, 
    type 
  }: { 
    reservation: any; 
    type: 'checkin' | 'checkout' | 'current' | 'early' | 'late'
  }) => {
    const guest = guests.find(g => g.id === reservation.guest_id);
    const room = rooms.find(r => r.id === reservation.room_id);
    const isProcessing = processingReservations.has(reservation.id);
    
    if (!guest || !room) return null;

    const getTypeConfig = () => {
      switch (type) {
        case 'checkin':
          return { icon: Calendar, color: 'text-green-600', label: 'Llegada Hoy', bgColor: 'bg-green-600' };
        case 'checkout':
          return { icon: Clock, color: 'text-blue-600', label: 'Salida Hoy', bgColor: 'bg-blue-600' };
        case 'current':
          return { icon: CheckCircle, color: 'text-green-600', label: 'En Hotel', bgColor: 'bg-green-600' };
        case 'early':
          return { icon: Calendar, color: 'text-orange-600', label: 'Check-in Anticipado', bgColor: 'bg-orange-600' };
        case 'late':
          return { icon: AlertTriangle, color: 'text-red-600', label: 'Check-out Tard√≠o', bgColor: 'bg-red-600' };
      }
    };

    const config = getTypeConfig();
    const Icon = config.icon;

    return (
      <Card className="w-full">
        <CardContent className="p-4 space-y-4">
          {/* Header */}
          <div className="flex items-center justify-between">
            <div className="flex items-center gap-2">
              <Icon className={`h-4 w-4 ${config.color}`} />
              <span className="text-sm font-medium">{config.label}</span>
            </div>
            <Badge variant="outline" className="text-xs">#{reservation.id.slice(0, 8)}</Badge>
          </div>

          {/* Guest Info */}
          <div className="flex items-center gap-3">
            <User className="h-5 w-5 text-purple-600" />
            <div>
              <div className="font-medium">{guest.first_name} {guest.last_name}</div>
              <div className="text-sm text-muted-foreground">{guest.email}</div>
            </div>
          </div>

          {/* Room Info */}
          <div className="flex items-center gap-3">
            <MapPin className="h-5 w-5 text-green-600" />
            <div>
              <div className="font-medium">Habitaci√≥n #{room.number}</div>
              <div className="text-sm text-muted-foreground capitalize">
                {room.type.replace('-', ' ')} ‚Ä¢ {reservation.guests_count} hu√©sped{reservation.guests_count > 1 ? 'es' : ''}
              </div>
            </div>
          </div>

          {/* Dates */}
          <div className="flex justify-between text-sm">
            <div>
              <span className="text-muted-foreground">Entrada: </span>
              <span className="font-medium">
                {format(new Date(reservation.check_in), 'dd/MM/yyyy', { locale: es })}
              </span>
            </div>
            <div>
              <span className="text-muted-foreground">Salida: </span>
              <span className="font-medium">
                {format(new Date(reservation.check_out), 'dd/MM/yyyy', { locale: es })}
              </span>
            </div>
          </div>

          {/* Action Button */}
          <div className="pt-2 border-t">
            {(type === 'checkin' || type === 'early') && (
              <Button
                onClick={() => handleCheckIn(reservation.id)}
                disabled={isProcessing}
                className={`w-full ${config.bgColor} hover:opacity-90 text-white`}
              >
                {isProcessing ? (
                  <>
                    <Clock className="h-4 w-4 mr-2 animate-spin" />
                    Procesando...
                  </>
                ) : (
                  <>
                    <CheckCircle className="h-4 w-4 mr-2" />
                    Check-in
                  </>
                )}
              </Button>
            )}
            
            {(type === 'checkout' || type === 'late') && (
              <Button
                onClick={() => handleCheckOut(reservation.id)}
                disabled={isProcessing}
                className={`w-full ${config.bgColor} hover:opacity-90 text-white`}
              >
                {isProcessing ? (
                  <>
                    <Clock className="h-4 w-4 mr-2 animate-spin" />
                    Procesando...
                  </>
                ) : (
                  <>
                    <Clock className="h-4 w-4 mr-2" />
                    Check-out
                  </>
                )}
              </Button>
            )}
            
            {type === 'current' && (
              <div className="flex gap-2">
                <Badge variant="outline" className="flex-1 justify-center text-green-600 border-green-600">
                  <CheckCircle className="h-3 w-3 mr-1" />
                  En Hotel
                </Badge>
                <Button
                  onClick={() => handleCheckOut(reservation.id)}
                  disabled={isProcessing}
                  variant="outline"
                  size="sm"
                  className="text-blue-600 border-blue-600 hover:bg-blue-50"
                >
                  {isProcessing ? 'Procesando...' : 'Check-out'}
                </Button>
              </div>
            )}
          </div>
        </CardContent>
      </Card>
    );
  };

  if (isLoading) {
    return (
      <div className="space-y-6">
        <div className="flex items-center justify-between">
          <div>
            <h1 className="text-3xl font-bold tracking-tight">Check-in / Check-out</h1>
            <p className="text-muted-foreground">Cargando informaci√≥n...</p>
          </div>
          <BackToHomeButton />
        </div>
        <div className="flex items-center justify-center h-64">
          <div className="text-lg">Cargando datos...</div>
        </div>
      </div>
    );
  }

  return (
    <div className="space-y-6">
      <div className="flex items-center justify-between">
        <div>
          <h1 className="text-3xl font-bold tracking-tight">Check-in / Check-out</h1>
          <p className="text-muted-foreground">
            Sistema optimizado - Actualizaciones autom√°ticas
          </p>
        </div>
        <BackToHomeButton />
      </div>

      {/* Stats Cards */}
      <div className="grid grid-cols-2 md:grid-cols-5 gap-4">
        <Card>
          <CardHeader className="pb-2">
            <CardTitle className="text-sm font-medium text-green-600">Check-ins Hoy</CardTitle>
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold">{todayCheckIns.length}</div>
          </CardContent>
        </Card>
        
        <Card>
          <CardHeader className="pb-2">
            <CardTitle className="text-sm font-medium text-blue-600">Check-outs Hoy</CardTitle>
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold">{todayCheckOuts.length}</div>
          </CardContent>
        </Card>

        <Card>
          <CardHeader className="pb-2">
            <CardTitle className="text-sm font-medium text-purple-600">En Hotel</CardTitle>
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold">{currentGuests.length}</div>
          </CardContent>
        </Card>

        <Card>
          <CardHeader className="pb-2">
            <CardTitle className="text-sm font-medium text-orange-600">Anticipados</CardTitle>
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold">{earlyCheckIns.length}</div>
          </CardContent>
        </Card>

        <Card>
          <CardHeader className="pb-2">
            <CardTitle className="text-sm font-medium text-red-600">Tard√≠os</CardTitle>
          </CardHeader>
          <CardContent>
            <div className="text-2xl font-bold">{lateCheckOuts.length}</div>
          </CardContent>
        </Card>
      </div>

      {/* Check-ins for Today */}
      {todayCheckIns.length > 0 && (
        <Card>
          <CardHeader>
            <CardTitle className="flex items-center gap-2">
              <Calendar className="h-5 w-5 text-green-600" />
              Check-ins de Hoy ({todayCheckIns.length})
            </CardTitle>
          </CardHeader>
          <CardContent>
            <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-4">
              {todayCheckIns.map((reservation) => (
                <ReservationCard
                  key={reservation.id}
                  reservation={reservation}
                  type="checkin"
                />
              ))}
            </div>
          </CardContent>
        </Card>
      )}

      {/* Check-outs for Today */}
      {todayCheckOuts.length > 0 && (
        <Card>
          <CardHeader>
            <CardTitle className="flex items-center gap-2">
              <Clock className="h-5 w-5 text-blue-600" />
              Check-outs de Hoy ({todayCheckOuts.length})
            </CardTitle>
          </CardHeader>
          <CardContent>
            <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-4">
              {todayCheckOuts.map((reservation) => (
                <ReservationCard
                  key={reservation.id}
                  reservation={reservation}
                  type="checkout"
                />
              ))}
            </div>
          </CardContent>
        </Card>
      )}

      {/* Current Guests */}
      {currentGuests.length > 0 && (
        <Card>
          <CardHeader>
            <CardTitle className="flex items-center gap-2">
              <User className="h-5 w-5 text-purple-600" />
              Hu√©spedes Actuales ({currentGuests.length})
            </CardTitle>
          </CardHeader>
          <CardContent>
            <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-4">
              {currentGuests.map((reservation) => (
                <ReservationCard
                  key={reservation.id}
                  reservation={reservation}
                  type="current"
                />
              ))}
            </div>
          </CardContent>
        </Card>
      )}

      {/* Early Check-ins */}
      {earlyCheckIns.length > 0 && (
        <Card>
          <CardHeader>
            <CardTitle className="flex items-center gap-2">
              <Calendar className="h-5 w-5 text-orange-600" />
              Check-ins Anticipados ({earlyCheckIns.length})
            </CardTitle>
          </CardHeader>
          <CardContent>
            <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-4">
              {earlyCheckIns.map((reservation) => (
                <ReservationCard
                  key={reservation.id}
                  reservation={reservation}
                  type="early"
                />
              ))}
            </div>
          </CardContent>
        </Card>
      )}

      {/* Late Check-outs */}
      {lateCheckOuts.length > 0 && (
        <Card>
          <CardHeader>
            <CardTitle className="flex items-center gap-2">
              <AlertTriangle className="h-5 w-5 text-red-600" />
              Check-outs Tard√≠os ({lateCheckOuts.length})
            </CardTitle>
          </CardHeader>
          <CardContent>
            <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-4">
              {lateCheckOuts.map((reservation) => (
                <ReservationCard
                  key={reservation.id}
                  reservation={reservation}
                  type="late"
                />
              ))}
            </div>
          </CardContent>
        </Card>
      )}

      {/* Empty State */}
      {todayCheckIns.length === 0 && todayCheckOuts.length === 0 && currentGuests.length === 0 && earlyCheckIns.length === 0 && lateCheckOuts.length === 0 && (
        <Card>
          <CardContent className="text-center py-12">
            <CheckCircle className="h-16 w-16 mx-auto mb-4 text-green-500" />
            <h3 className="text-lg font-semibold mb-2">Sin actividad pendiente</h3>
            <p className="text-muted-foreground">No hay check-ins ni check-outs programados para hoy</p>
          </CardContent>
        </Card>
      )}
    </div>
  );
};

export default CheckInOutPage;
